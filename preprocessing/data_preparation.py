def build_binary_single_dataset_frame(
    raw_frame: pd.DataFrame,
    split_name: str,
    config: SentraGuardConfig,
) -> pd.DataFrame:
    """
    Constructs a binary-labeled DataFrame from raw JailBreakV-28K rows.
    Each harmful prompt is paired with a benign counterfactual,
    producing a balanced binary classification dataset.
    """
    base = raw_frame.copy().reset_index(drop=True)

    if "id" not in base.columns:
        base["id"] = [f"{split_name}_{idx:07d}" for idx in range(len(base))]

    base["origin_id"] = base["id"].fillna("").astype(str).str.strip()
    base["harmful_text"] = base["jailbreak_query"].fillna("").astype(str).str.strip()
    base["safety_seed_text"] = (
        base.get("redteam_query", base["jailbreak_query"])
        .fillna("")
        .astype(str)
        .str.strip()
    )

    base["policy"] = (
        base["policy"].fillna("unknown").astype(str).str.strip()
        if "policy" in base.columns
        else "unknown"
    )
    base["format"] = (
        base["format"].fillna("unknown").astype(str).str.strip()
        if "format" in base.columns
        else "unknown"
    )
    base["from"] = (
        base["from"].fillna("unknown").astype(str).str.strip()
        if "from" in base.columns
        else "unknown"
    )
    if "transfer_from_llm" in base.columns:
        base["transfer_from_llm"] = base["transfer_from_llm"].fillna(False).astype(bool)
    else:
        base["transfer_from_llm"] = False

    base = base.loc[base["harmful_text"].astype(bool)].reset_index(drop=True)

    harmful_frame = pd.DataFrame(
        {
            "sample_id": [f"{split_name}_H_{idx:07d}" for idx in range(len(base))],
            "origin_id": base["origin_id"].tolist(),
            "text": base["harmful_text"].tolist(),
            "label": 1,
            "policy": base["policy"].tolist(),
            "format": base["format"].tolist(),
            "from_source": base["from"].tolist(),
            "transfer_from_llm": base["transfer_from_llm"].tolist(),
            "split_name": split_name,
            "variant": "harmful",
        }
    )

    benign_texts = []
    for idx, query in enumerate(base["safety_seed_text"].tolist()):
        safe_query = query if query else base["harmful_text"].iloc[idx]
        template = SAFE_RESPONSE_TEMPLATES[idx % len(SAFE_RESPONSE_TEMPLATES)]
        benign_texts.append(template.format(query=safe_query))

    benign_frame = pd.DataFrame(
        {
            "sample_id": [f"{split_name}_B_{idx:07d}" for idx in range(len(base))],
            "origin_id": base["origin_id"].tolist(),
            "text": benign_texts,
            "label": 0,
            "policy": base["policy"].tolist(),
            "format": base["format"].tolist(),
            "from_source": base["from"].tolist(),
            "transfer_from_llm": [False] * len(base),
            "split_name": split_name,
            "variant": "benign_counterfactual",
        }
    )

    binary_frame = pd.concat([harmful_frame, benign_frame], ignore_index=True)
    binary_frame["text"] = binary_frame["text"].fillna("").astype(str).str.strip()
    binary_frame["label"] = pd.to_numeric(binary_frame["label"], errors="coerce")
    binary_frame = binary_frame.loc[binary_frame["text"].astype(bool)].copy()
    binary_frame = binary_frame.loc[binary_frame["label"].isin([0, 1])].copy()
    binary_frame["label"] = binary_frame["label"].astype(int)

    required_columns = [
        "sample_id", "origin_id", "text", "label", "policy",
        "format", "from_source", "transfer_from_llm", "split_name", "variant",
    ]
    binary_frame = (
        binary_frame[required_columns]
        .drop_duplicates(subset=["sample_id"])
        .reset_index(drop=True)
    )
    binary_frame = binary_frame.sample(
        frac=1.0, random_state=config.random_seed
    ).reset_index(drop=True)
    return binary_frame


def make_stratify_labels(frame: pd.DataFrame) -> Optional[pd.Series]:
    """
    Returns a stratification Series for train_test_split if feasible,
    else returns None.
    """
    if "policy" not in frame.columns:
        return None
    candidate = frame["policy"].fillna("unknown").astype(str)
    counts = candidate.value_counts()
    if len(counts) > 1 and counts.min() >= 2:
        return candidate
    return None


def split_dataset(
    raw_df: pd.DataFrame,
    config: SentraGuardConfig,
):
    """
    Performs stratified train/validation/test splitting of the raw DataFrame
    and constructs binary classification DataFrames for each split.
    Returns (train_df, val_df, test_df).
    """
    jbv_raw_df = raw_df.copy()
    jbv_raw_df["jailbreak_query"] = (
        jbv_raw_df["jailbreak_query"].fillna("").astype(str).str.strip()
    )
    jbv_text_df = jbv_raw_df.loc[
        jbv_raw_df["jailbreak_query"].astype(bool)
    ].reset_index(drop=True)

    first_stratify = make_stratify_labels(jbv_text_df)
    train_raw_df, temp_raw_df = train_test_split(
        jbv_text_df,
        test_size=(1.0 - config.train_ratio),
        random_state=config.random_seed,
        stratify=first_stratify,
    )

    second_stratify = make_stratify_labels(temp_raw_df)
    val_raw_df, test_raw_df = train_test_split(
        temp_raw_df,
        test_size=0.50,
        random_state=config.random_seed,
        stratify=second_stratify,
    )

    train_df = build_binary_single_dataset_frame(
        train_raw_df.reset_index(drop=True), "train", config
    )
    val_df = build_binary_single_dataset_frame(
        val_raw_df.reset_index(drop=True), "validation", config
    )
    test_df = build_binary_single_dataset_frame(
        test_raw_df.reset_index(drop=True), "test", config
    )

    return train_df, val_df, test_df, jbv_text_df, train_raw_df, val_raw_df, test_raw_df


