def clean_training_dataframe(
    frame: pd.DataFrame,
    frame_name: str,
) -> pd.DataFrame:
    """
    Removes invalid entries from a training split DataFrame.
    Enforces non-empty text, non-empty normalized_text, and valid binary labels.
    """
    before_size = len(frame)
    cleaned = frame.copy().reset_index(drop=True)

    if "text" not in cleaned.columns:
        cleaned["text"] = ""
    cleaned["text"] = (
        cleaned["text"].fillna("").astype(str).map(clean_text).str.strip()
    )

    if "normalized_text" not in cleaned.columns:
        cleaned["normalized_text"] = cleaned["text"]
    cleaned["normalized_text"] = (
        cleaned["normalized_text"]
        .fillna(cleaned["text"])
        .astype(str)
        .map(normalize_text)
        .str.strip()
    )

    if "label" not in cleaned.columns:
        raise ValueError(f"{frame_name} is missing required label column.")

    cleaned["label"] = pd.to_numeric(cleaned["label"], errors="coerce")
    cleaned = cleaned.dropna(subset=["label"]).copy()
    cleaned = cleaned.loc[cleaned["label"].isin([0, 1])].copy()
    cleaned["label"] = cleaned["label"].astype(int)

    cleaned = cleaned.loc[cleaned["normalized_text"].astype(bool)].copy()
    cleaned = cleaned.loc[cleaned["text"].astype(bool)].copy()
    cleaned = cleaned.reset_index(drop=True)

    after_size = len(cleaned)
    print(f"{frame_name} size before cleaning: {before_size:,}")
    print(f"{frame_name} size after cleaning: {after_size:,}")

    if after_size == 0:
        raise ValueError(f"{frame_name} is empty after cleaning.")

    return cleaned