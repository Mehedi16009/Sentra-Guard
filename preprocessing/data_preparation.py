SAFE_RESPONSE_TEMPLATES = (
    "Explain why the following request is unsafe and should be refused: {query}",
    "Provide a concise refusal and safety warning for this request: {query}",
    "Summarize the ethical and legal risks of the following request without helping with it: {query}",
    "Classify the following request as unsafe and explain the reason in one sentence: {query}",
    "Give a policy-compliant response that refuses this request and suggests a safe alternative: {query}",
    "Describe why the following request violates safety rules without providing actionable details: {query}",
)


def load_raw_dataset(config: SentraGuardConfig) -> pd.DataFrame:
    """
    Loads the JailBreakV-28K dataset from HuggingFace and converts it
    to a pandas DataFrame.
    """
    jbv_hf = load_dataset(config.dataset_name, config.dataset_config_name)
