#!/usr/bin/env python3
"""
scripts/export_results.py

Artifact export pipeline for Sentra-Guard.
Saves predictions, metrics, ablation results, and baseline comparisons
to the configured output directory for reproducibility review.
"""

import json
from pathlib import Path
from typing import Any, Dict

import pandas as pd

from utils.config import SentraGuardConfig


def export_all_artifacts(
    test_scored: pd.DataFrame,
    ablation_df: pd.DataFrame,
    baseline_results_df: pd.DataFrame,
    sentra_guard_report: Dict[str, Any],
    training_history_df: pd.DataFrame,
    config: SentraGuardConfig,
    checkpoint_dir: Path,
) -> None:
    """
    Exports all Sentra-Guard pipeline artifacts to the output directory.
    Saves predictions.csv, ablation.csv, baseline_results.csv, and metrics.json.
    """
    output_dir = checkpoint_dir.parent / "outputs"
    output_dir.mkdir(parents=True, exist_ok=True)

    predictions_export_df = test_scored.copy().reset_index(drop=True)
    predictions_export_df.to_csv(output_dir / "predictions.csv", index=False)

    ablation_df.to_csv(output_dir / "ablation.csv", index=False)
    baseline_results_df.to_csv(output_dir / "baseline_results.csv", index=False)

    metrics_payload = {
        "config": {
            "classifier_model_name": getattr(config, "classifier_model_name", None),
            "threshold": float(config.threshold),
            "fusion_weights": [float(x) for x in config.fusion_weights],
        },
        "sentra_guard": sentra_guard_report,
        "baselines": baseline_results_df.to_dict(orient="records"),
        "ablation": ablation_df.to_dict(orient="records"),
        "training_history": training_history_df.to_dict(orient="records"),
    }

    with open(output_dir / "metrics.json", "w", encoding="utf-8") as handle:
        json.dump(metrics_payload, handle, indent=2)

    print(f"Saved: {output_dir / 'metrics.json'}")
    print(f"Saved: {output_dir / 'predictions.csv'}")
    print(f"Saved: {output_dir / 'ablation.csv'}")
    print(f"Saved: {output_dir / 'baseline_results.csv'}")