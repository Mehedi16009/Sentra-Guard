def translate_to_english(text: str, source_lang: Optional[str] = None) -> str:
    normalized = normalize_text(text)
    if not normalized:
        return ""
    detected_language = canonicalize_language(source_lang) if source_lang else detect_language_safe(normalized)
    translated = translator_manager.translate_batch([normalized], detected_language)[0]
    return normalize_text(translated)

def preprocess_text(text: str, source_lang: Optional[str] = None) -> Dict[str, str]:
    normalized_source_text = normalize_text(text)
    if not normalized_source_text:
        return {
            "clean_text": "",
            "normalized_text": "",
            "detected_language": "unknown",
            "normalization_status": "empty",
        }

    detected_language = canonicalize_language(source_lang) if source_lang else detect_language_safe(normalized_source_text)
    if detected_language == "en":
        translated_text = normalized_source_text
        status = "english_pass"
    elif detected_language in config.translation_models:
        translated_text = translate_to_english(normalized_source_text, detected_language)
        status = "translated"
    else:
        translated_text = normalized_source_text
        status = "translation_unavailable"

    return {
        "clean_text": normalized_source_text,
        "normalized_text": normalize_text(translated_text),
        "detected_language": detected_language,
        "normalization_status": status,
    }

def preprocess_dataframe(frame: pd.DataFrame) -> pd.DataFrame:
    processed = frame.copy().reset_index(drop=True)
    processed["text"] = processed["text"].fillna("").astype(str).map(normalize_text)

    if "label" in processed.columns:
        processed["label"] = pd.to_numeric(processed["label"], errors="coerce")
        processed = processed.loc[processed["label"].isin([0, 1])].copy()
        processed["label"] = processed["label"].astype(int)

    processed = processed.loc[processed["text"].astype(bool)].copy().reset_index(drop=True)
    if processed.empty:
        raise ValueError("All rows were removed during preprocessing; no valid text rows remain.")

    unique_texts = processed["text"].dropna().astype(str).unique().tolist()
    language_map = {text: detect_language_safe(text) if text else "unknown" for text in unique_texts}

    translated_map: Dict[str, str] = {}
    status_map: Dict[str, str] = {}
    grouped: Dict[str, List[str]] = {}

    for text, language in language_map.items():
        grouped.setdefault(language, []).append(text)

    for language, texts in grouped.items():
        if language == "en":
            for text in texts:
                translated_map[text] = text
                status_map[text] = "english_pass"
        elif language in config.translation_models:
            translated_texts = translator_manager.translate_batch(texts, language)
            for original, translated in zip(texts, translated_texts):
                safe_translated = normalize_text(translated)
                translated_map[original] = safe_translated if safe_translated else original
                status_map[original] = "translated"
        else:
            for text in texts:
                translated_map[text] = text
                status_map[text] = "translation_unavailable"

    processed["detected_language"] = processed["text"].map(language_map)
    processed["normalized_text"] = processed["text"].map(translated_map).fillna("")
    processed["normalized_text"] = processed["normalized_text"].astype(str).map(normalize_text)
    processed["normalization_status"] = processed["text"].map(status_map).fillna("unknown")

    processed = processed.loc[processed["normalized_text"].astype(bool)].copy().reset_index(drop=True)
    if "label" in processed.columns:
        processed = processed.loc[processed["label"].isin([0, 1])].copy().reset_index(drop=True)
        processed["label"] = processed["label"].astype(int)

    if processed.empty:
        raise ValueError("All rows were removed after normalization; no valid normalized_text rows remain.")

    return processed

