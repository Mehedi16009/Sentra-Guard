def train_epoch(
    model: AutoModelForSequenceClassification,
    dataloader: DataLoader,
    optimizer: AdamW,
    scheduler: Any,
    criterion: nn.Module,
    device: str,
) -> Dict[str, float]:
    model.train()

    running_losses: List[float] = []
    all_true: List[int] = []
    all_pred: List[int] = []

    for batch_idx, batch in enumerate(tqdm(dataloader, desc="Training", leave=False)):
        raw_text = batch["raw_text"]
        labels = batch["labels"].to(device)
        input_ids = batch["input_ids"].to(device)
        attention_mask = batch["attention_mask"].to(device)

        assert torch.isfinite(labels.float()).all(), f"Non-finite labels at training batch {batch_idx}"
        assert torch.isfinite(input_ids.float()).all(), f"Non-finite input_ids at training batch {batch_idx}"
        assert torch.isfinite(attention_mask.float()).all(), f"Non-finite attention_mask at training batch {batch_idx}"

        optimizer.zero_grad(set_to_none=True)

        outputs = model(
            input_ids=input_ids,
            attention_mask=attention_mask,
        )
        logits = outputs.logits.float()

        if not torch.isfinite(logits).all():
            _print_invalid_batch_state(
                {
                    "raw_text": raw_text,
                    "input_ids": input_ids,
                    "labels": labels,
                },
                logits=logits,
            )
            raise AssertionError(f"Non-finite logits at training batch {batch_idx}")

        loss = criterion(logits, labels)

        if not torch.isfinite(loss):
            _print_invalid_batch_state(
                {
                    "raw_text": raw_text,
                    "input_ids": input_ids,
                    "labels": labels,
                },
                logits=logits,
                loss=loss,
            )
            raise AssertionError(f"Non-finite loss at training batch {batch_idx}")

        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        optimizer.step()
        scheduler.step()

        running_losses.append(float(loss.item()))

        preds = torch.argmax(logits, dim=-1)
        all_true.extend(labels.detach().cpu().numpy().astype(int).tolist())
        all_pred.extend(preds.detach().cpu().numpy().astype(int).tolist())

    metrics = _compute_epoch_metrics(all_true, all_pred)
    return {
        "train_loss": float(np.mean(running_losses)) if running_losses else 0.0,
        "train_accuracy": metrics["accuracy"],
        "train_precision": metrics["precision"],
        "train_recall": metrics["recall"],
        "train_f1": metrics["f1"],
        "skipped_batches": 0,
    }

