def score_split(
    frame: pd.DataFrame,
    split_name: str,
    weights: Optional[Tuple[float, float, float]] = None,
    threshold: Optional[float] = None,
) -> pd.DataFrame:
    safe_frame = frame.copy().reset_index(drop=True)
    safe_frame["text"] = _get_text_series(safe_frame)

    classifier_scores = compute_classifier_scores(safe_frame["text"].tolist())
    retrieval_scores = compute_retrieval_scores(safe_frame["text"].tolist())
    zero_shot_scores = compute_zero_shot_scores(safe_frame["text"].tolist())

    weights = tuple(weights or config.fusion_weights)
    threshold = float(config.threshold if threshold is None else threshold)

    final_scores = compute_final_scores(
        classifier_scores,
        retrieval_scores,
        zero_shot_scores,
        weights,
    )

    pred_label = (final_scores >= threshold).astype(int)
    pred_label_name = np.where(pred_label == 1, "harmful", "benign")

    branch_votes = np.column_stack(
        [
            (classifier_scores >= 0.50).astype(int),
            (retrieval_scores >= 0.50).astype(int),
            (zero_shot_scores >= 0.50).astype(int),
        ]
    )
    branch_disagreement = np.ptp(branch_votes, axis=1) > 0
    threshold_margin = np.abs(final_scores - threshold) <= float(getattr(config, "uncertainty_margin", 0.08))
    needs_hitl = branch_disagreement | threshold_margin

    scored_df = safe_frame.copy()
    scored_df["split_name"] = split_name
    scored_df["classifier_score"] = classifier_scores
    scored_df["retrieval_score"] = retrieval_scores
    scored_df["zero_shot_score"] = zero_shot_scores
    scored_df["final_score"] = final_scores
    scored_df["pred_label"] = pred_label.astype(int)
    scored_df["pred_label_name"] = pred_label_name
    scored_df["needs_hitl"] = needs_hitl.astype(bool)
    scored_df["threshold"] = threshold
    scored_df["w1"] = float(weights[0])
    scored_df["w2"] = float(weights[1])
    scored_df["w3"] = float(weights[2])

    return scored_df

