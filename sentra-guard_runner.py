def compute_class_weights(labels: Sequence[int], device: str, eps: float = 1e-8) -> torch.Tensor:
    label_array = pd.to_numeric(pd.Series(labels), errors="coerce").to_numpy()
    valid_mask = np.isfinite(label_array) & np.isin(label_array, [0, 1])
    label_array = label_array[valid_mask].astype(np.int64)

    if label_array.size == 0:
        return torch.ones(2, dtype=torch.float32, device=device)

    class_counts = np.bincount(label_array, minlength=2).astype(np.float32)
    class_counts = np.maximum(class_counts, eps)
    class_weights = label_array.shape[0] / (len(class_counts) * class_counts)
    class_weights = np.nan_to_num(class_weights, nan=1.0, posinf=1.0, neginf=1.0)
    class_weights = np.clip(class_weights, eps, None)
    class_weights = class_weights / max(class_weights.mean(), eps)
    return torch.tensor(class_weights, dtype=torch.float32, device=device)

