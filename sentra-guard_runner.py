def compute_zero_shot_scores(
    texts: Sequence[str],
    batch_size: int = 8,
) -> np.ndarray:
    safe_texts = [str(text).strip() if text is not None else "" for text in texts]
    if len(safe_texts) == 0:
        return np.asarray([], dtype=np.float32)

    outputs: List[float] = []

    for start in range(0, len(safe_texts), batch_size):
        batch_texts = [text if text else "[EMPTY]" for text in safe_texts[start : start + batch_size]]
        batch_results = zero_shot_classifier(
            batch_texts,
            candidate_labels=zero_shot_candidate_labels,
            multi_label=False,
            batch_size=batch_size,
        )

        if isinstance(batch_results, dict):
            batch_results = [batch_results]

        for result in batch_results:
            label_score_map = {label: float(score) for label, score in zip(result["labels"], result["scores"])}
            harmful_score = label_score_map.get("harmful", 0.0)
            harmful_score = float(np.clip(np.nan_to_num(harmful_score, nan=0.0, posinf=1.0, neginf=0.0), 0.0, 1.0))
            outputs.append(harmful_score)

    return np.asarray(outputs, dtype=np.float32)

