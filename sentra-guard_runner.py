CONTROL_CHAR_RE = re.compile(r"[\u200b-\u200f\u202a-\u202e\u2060\u2066-\u2069]")
MULTISPACE_RE = re.compile(r"\s+")

def clean_text(text: Any) -> str:
    if text is None or (isinstance(text, float) and np.isnan(text)):
        return ""
    text = str(text)
    text = unicodedata.normalize("NFKC", text)
    text = CONTROL_CHAR_RE.sub(" ", text)
    text = text.replace("\x00", " ")
    text = MULTISPACE_RE.sub(" ", text).strip()
    return text

"""# ------------------------------
# Language Processing Helpers
# Normalize multilingual inputs
# ------------------------------

Standardizes multilingual text into model-ready representations.
"""

LANGUAGE_ALIASES = {
    "en": "en", "english": "en",
    "fr": "fr", "french": "fr",
    "es": "es", "spanish": "es",
    "ar": "ar", "arabic": "ar",
    "hi": "hi", "hindi": "hi",
    "bn": "bn", "bengali": "bn",
    "zh": "zh", "chinese": "zh",
}

def normalize_text(text: Any) -> str:
    text = clean_text(text)
    text = re.sub(r"^\s*(system|assistant|developer|tool)\s*:\s*", "", text, flags=re.IGNORECASE)
    text = text.strip("`\"' ")
    text = MULTISPACE_RE.sub(" ", text).strip()
    return text

def canonicalize_language(language: Optional[str]) -> str:
    if language is None:
        return "unknown"
    language = str(language).strip().lower()
    return LANGUAGE_ALIASES.get(language, language.split("-")[0] if language else "unknown")

def detect_language_safe(text: str) -> str:
    text = normalize_text(text)
    if not text:
        return "unknown"
    ascii_letters = sum(char.isascii() and (char.isalpha() or char.isspace()) for char in text[:512])
    sample_length = max(len(text[:512]), 1)
    if (ascii_letters / sample_length) >= 0.85:
        return "en"
    try:
        return canonicalize_language(detect(text[:512]))
    except LangDetectException:
        return "unknown"

class TranslationManager:
    def __init__(self, model_map: Dict[str, str], device: str, batch_size: int = 16) -> None:
        self.model_map = model_map
        self.device = device
        self.batch_size = batch_size
        self.model_cache: Dict[str, MarianMTModel] = {}
        self.tokenizer_cache: Dict[str, MarianTokenizer] = {}

    def _load_model(self, source_lang: str) -> Tuple[MarianTokenizer, MarianMTModel]:
        model_name = self.model_map[source_lang]
        if source_lang not in self.model_cache:
            tokenizer = MarianTokenizer.from_pretrained(model_name)
            model = MarianMTModel.from_pretrained(model_name).to(self.device)
            model.eval()
            self.tokenizer_cache[source_lang] = tokenizer
            self.model_cache[source_lang] = model
        return self.tokenizer_cache[source_lang], self.model_cache[source_lang]

    @torch.inference_mode()
    def translate_batch(self, texts: Sequence[str], source_lang: str) -> List[str]:
        safe_texts = [normalize_text(text) for text in texts]
        if source_lang == "en" or source_lang not in self.model_map:
            return safe_texts

        tokenizer, model = self._load_model(source_lang)
        translated_texts: List[str] = []
        for start in range(0, len(safe_texts), self.batch_size):
            batch = safe_texts[start : start + self.batch_size]
            encoded = tokenizer(
                batch,
                return_tensors="pt",
                padding=True,
                truncation=True,
                max_length=256,
            )
            encoded = {key: value.to(self.device) for key, value in encoded.items()}
            generated = model.generate(**encoded, max_length=256, num_beams=4)
            translated_texts.extend(tokenizer.batch_decode(generated, skip_special_tokens=True))
        return [normalize_text(text) for text in translated_texts]

