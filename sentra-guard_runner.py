class SafeCrossEntropyLoss(nn.Module):
    def __init__(self, class_weights: torch.Tensor) -> None:
        super().__init__()
        safe_weights = torch.nan_to_num(
            class_weights.detach().float(),
            nan=1.0,
            posinf=1.0,
            neginf=1.0,
        ).clamp(min=1e-6)
        self.register_buffer("class_weights", safe_weights)

    def forward(self, logits: torch.Tensor, targets: torch.Tensor) -> torch.Tensor:
        safe_logits = torch.nan_to_num(
            logits.float(),
            nan=0.0,
            posinf=30.0,
            neginf=-30.0,
        )
        safe_targets = targets.long()
        safe_weights = self.class_weights.to(device=safe_logits.device, dtype=safe_logits.dtype)
        return F.cross_entropy(safe_logits, safe_targets, weight=safe_weights)


