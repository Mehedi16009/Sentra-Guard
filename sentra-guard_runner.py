def generate_weight_grid(step: float = 0.1) -> List[Tuple[float, float, float]]:
    values = np.round(np.arange(0.0, 1.0 + step, step), 2)
    grid: List[Tuple[float, float, float]] = []

    for w1 in values:
        for w2 in values:
            w3 = round(1.0 - w1 - w2, 2)
            if w3 < 0.0 or w3 > 1.0:
                continue
            if abs((w1 + w2 + w3) - 1.0) > 1e-8:
                continue
            grid.append((float(w1), float(w2), float(w3)))

    return sorted(set(grid))

