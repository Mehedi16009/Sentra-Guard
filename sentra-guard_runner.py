class PromptDataset(Dataset):
    def __init__(
        self,
        texts: Sequence[str],
        labels: Optional[Sequence[int]],
        tokenizer: AutoTokenizer,
        max_length: int,
    ) -> None:
        self.tokenizer = tokenizer
        self.max_length = int(max_length)

        safe_texts = pd.Series(list(texts), dtype="object").fillna("").astype(str).map(clean_text).str.strip().tolist()
        safe_texts = [text if text else "[EMPTY]" for text in safe_texts]
        self.texts = safe_texts

        if labels is None:
            self.labels = None
        else:
            label_series = pd.to_numeric(pd.Series(list(labels)), errors="coerce")
            if label_series.isna().any():
                raise ValueError("PromptDataset received NaN labels.")
            if not label_series.isin([0, 1]).all():
                raise ValueError("PromptDataset received labels outside {0,1}.")
            self.labels = label_series.astype(int).tolist()

        if self.labels is not None and len(self.texts) != len(self.labels):
            raise ValueError("PromptDataset texts and labels length mismatch.")

    def __len__(self) -> int:
        return len(self.texts)

    def __getitem__(self, index: int) -> Dict[str, torch.Tensor]:
        raw_text = self.texts[index]
        encoded = self.tokenizer(
            raw_text,
            truncation=True,
            padding="max_length",
            max_length=self.max_length,
            return_tensors="pt",
        )

        input_ids = encoded["input_ids"].squeeze(0).long()
        attention_mask = encoded["attention_mask"].squeeze(0).long()

        if input_ids.numel() == 0 or attention_mask.numel() == 0:
            raise ValueError(f"Empty tokenized tensors at dataset index {index}.")
        if not torch.isfinite(input_ids.float()).all():
            raise ValueError(f"Non-finite input_ids at dataset index {index}.")
        if not torch.isfinite(attention_mask.float()).all():
            raise ValueError(f"Non-finite attention_mask at dataset index {index}.")
        if attention_mask.sum().item() <= 0:
            raise ValueError(f"All-zero attention mask at dataset index {index}.")

        item: Dict[str, Union[torch.Tensor, str]] = {
            "input_ids": input_ids,
            "attention_mask": attention_mask,
            "raw_text": raw_text,
        }

        if self.labels is not None:
            label_value = int(self.labels[index])
            if label_value not in (0, 1):
                raise ValueError(f"Invalid label {label_value} at dataset index {index}.")
            item["labels"] = torch.tensor(label_value, dtype=torch.long)

        return item


