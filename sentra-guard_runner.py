    k = int(top_k or getattr(config, "top_k", 5))
    k = max(1, min(k, faiss_index.ntotal))

    similarities, _ = faiss_index.search(query_embeddings, k)
    similarities = np.asarray(similarities, dtype=np.float32)

    scaled_similarities = np.clip((similarities + 1.0) / 2.0, 0.0, 1.0)
    retrieval_scores = scaled_similarities.mean(axis=1)
    retrieval_scores = np.nan_to_num(retrieval_scores, nan=0.0, posinf=1.0, neginf=0.0)
    retrieval_scores = np.clip(retrieval_scores, 0.0, 1.0).astype(np.float32)

    return retrieval_scores

