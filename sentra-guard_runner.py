def debug_training_state(
    batch: Optional[Dict[str, torch.Tensor]] = None,
    logits: Optional[torch.Tensor] = None,
    loss: Optional[torch.Tensor] = None,
    model: Optional[torch.nn.Module] = None,
    prefix: str = "debug",
) -> bool:
    issues: List[str] = []

    def check_tensor(name: str, tensor: torch.Tensor) -> None:
        if tensor is None:
            return
        if tensor.numel() == 0:
            issues.append(f"{name}: empty")
            return
        tensor_float = tensor.detach().float()
        if torch.isnan(tensor_float).any():
            issues.append(f"{name}: NaN")
        if torch.isinf(tensor_float).any():
            issues.append(f"{name}: Inf")

    if batch is not None:
        if "input_ids" in batch:
            check_tensor("input_ids", batch["input_ids"])
        if "attention_mask" in batch:
            check_tensor("attention_mask", batch["attention_mask"])
        if "labels" in batch:
            check_tensor("labels", batch["labels"])
            labels = batch["labels"]
            if (~((labels == 0) | (labels == 1))).any():
                issues.append("labels: invalid values")

    if logits is not None:
        check_tensor("logits", logits)

    if loss is not None:
        check_tensor("loss", loss.view(1))

    if model is not None:
        for name, param in model.named_parameters():
            if param.grad is None:
                continue
            grad = param.grad.detach()
            if grad.numel() == 0:
                continue
            if torch.isnan(grad).any():
                issues.append(f"gradient {name}: NaN")
                break
            if torch.isinf(grad).any():
                issues.append(f"gradient {name}: Inf")
                break

    if issues:
        print(f"[{prefix}] " + " | ".join(issues))
        return False
    return True

